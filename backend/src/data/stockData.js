const STOCK_DATABASE = [
  AAPL_DATA, MSFT_DATA, NVDA_DATA, AMZN_DATA, GOOGL_DATA,
];

export function getStockData(ticker) {
  return STOCK_DATABASE.find(s => s.ticker === ticker);
}

export function getSnapshot(ticker, periodId) {
  return getStockData(ticker)?.snapshots.find(s => s.periodId === periodId);
}

export function getAvailablePeriods(ticker) {
  return getStockData(ticker)?.snapshots.map(s => s.periodId) ?? [];
}
